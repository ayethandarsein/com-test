package com.panu.competitor.information.dto;

import java.util.Date;

/**
 *
 * @author BI3
 */
public class ReportGraphDTO {
    
    private int competitorInfoId;
    private String companyName;
    private Date period;
    private int plantCapacity;
    private int volumePerMonth;
    private int noOfTruck;
    private int noOfPlant;

    public ReportGraphDTO(int competitorInfoId, String companyName, Date period, int plantCapacity, int volumePerMonth, int noOfTruck, int noOfPlant) {
        this.competitorInfoId = competitorInfoId;
        this.companyName = companyName;
        this.period = period;
        this.plantCapacity = plantCapacity;
        this.volumePerMonth = volumePerMonth;
        this.noOfTruck = noOfTruck;
        this.noOfPlant = noOfPlant;
    }

    public ReportGraphDTO() {
    }
    
    

    public int getCompetitorInfoId() {
        return competitorInfoId;
    }

    public void setCompetitorInfoId(int competitorInfoId) {
        this.competitorInfoId = competitorInfoId;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public Date getPeriod() {
        return period;
    }

    public void setPeriod(Date period) {
        this.period = period;
    }

    public int getPlantCapacity() {
        return plantCapacity;
    }

    public void setPlantCapacity(int plantCapacity) {
        this.plantCapacity = plantCapacity;
    }

    public int getVolumePerMonth() {
        return volumePerMonth;
    }

    public void setVolumePerMonth(int volumePerMonth) {
        this.volumePerMonth = volumePerMonth;
    }

    public int getNoOfTruck() {
        return noOfTruck;
    }

    public void setNoOfTruck(int noOfTruck) {
        this.noOfTruck = noOfTruck;
    }

    public int getNoOfPlant() {
        return noOfPlant;
    }

    public void setNoOfPlant(int noOfPlant) {
        this.noOfPlant = noOfPlant;
    }
    
    
    
}
