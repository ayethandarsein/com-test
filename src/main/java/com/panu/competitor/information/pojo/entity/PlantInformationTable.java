package com.panu.competitor.information.pojo.entity;

import java.io.Serializable;

public class PlantInformationTable implements Serializable {

    /**
     *
     */
    private static final long serialVersionUID = 1L;
    private int plantId;
    private String location;
    private String zone;
    private String capabilities;
    private char hdbapprove;
    private int noOfTruck;
    private int capacity;
    private String leasePeriod;
    private String supportedBy;

    public PlantInformationTable(int plantId, String location, String zone, String capabilities, char hdbapprove) {
        this.plantId = plantId;
        this.location = location;
        this.zone = zone;
        this.capabilities = capabilities;
        this.hdbapprove = hdbapprove;

    }

    public PlantInformationTable(int plantId, String location, String zone, int noOfTruck, int capacity) {
        this.plantId = plantId;
        this.location = location;
        this.zone = zone;
        this.noOfTruck = noOfTruck;
        this.capacity = capacity;

    }

    public PlantInformationTable(int plantId, String location, String zone, char hdbapprove, String leasePeriod, String supportedBy) {
        this.plantId = plantId;
        this.location = location;
        this.zone = zone;
        this.leasePeriod = capabilities;
        this.hdbapprove = hdbapprove;
        this.leasePeriod = leasePeriod;
        this.supportedBy = supportedBy;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getZone() {
        return zone;
    }

    public void setZone(String zone) {
        this.zone = zone;
    }

    public int getPlantId() {
        return plantId;
    }

    public void setPlantId(int plantId) {
        this.plantId = plantId;
    }

    public String getCapabilities() {
        return capabilities;
    }

    public void setCapabilities(String capabilities) {
        this.capabilities = capabilities;
    }

    public char getHdbapprove() {
        return hdbapprove;
    }

    public void setHdbapprove(char hdbapprove) {
        this.hdbapprove = hdbapprove;
    }

    public int getNoOfTruck() {
        return noOfTruck;
    }

    public void setNoOfTruck(int noOfTruck) {
        this.noOfTruck = noOfTruck;
    }

    public String getLeasePeriod() {
        return leasePeriod;
    }

    public void setLeasePeriod(String leasePeriod) {
        this.leasePeriod = leasePeriod;
    }

    public String getSupportedBy() {
        return supportedBy;
    }

    public void setSupportedBy(String supportedBy) {
        this.supportedBy = supportedBy;
    }

    public int getCapacity() {
        return capacity;
    }

    public void setCapacity(int capacity) {
        this.capacity = capacity;
    }

}
